/**
 * Î™®Îã¨ ÎÇ¥Î∂ÄÏùò 'Ï†ÄÏû•' Î≤ÑÌäºÏùÑ Ï≤òÎ¶¨Ìï©ÎãàÎã§.
 */
function handleModalSave() {
    if (!selectedDateKey) return;

    const content = modalQtInput.value.trim();
    let data = loadQtData();

    // Ï†ÄÏû• Î°úÏßÅ
    if (content) {
        data[selectedDateKey] = content;
    } else {
        delete data[selectedDateKey]; // ÎÇ¥Ïö©Ïù¥ ÏóÜÏúºÎ©¥ ÏÇ≠Ï†ú
    }

    saveQtData(data);
    closeModal(); // Ï†ÄÏû• ÌõÑ Î™®Îã¨ Îã´Í∏∞
}

/**
 * Ïõî Î≥ÄÍ≤Ω Î≤ÑÌäº ÌÅ¥Î¶≠ Ïãú Ïã§ÌñâÎê©ÎãàÎã§.
 */
function changeMonth(delta) {
    currentMonth += delta;
    if (currentMonth > 11) {
        currentMonth = 0;
        currentYear++;
    } else if (currentMonth < 0) {
        currentMonth = 11;
        currentYear--;
    }
    
    selectedDateKey = ''; 

    renderCalendar();
}

